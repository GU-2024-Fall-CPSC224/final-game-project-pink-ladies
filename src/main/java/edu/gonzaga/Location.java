package edu.gonzaga;

public class Location {
    int x;
    int y;
    public Location(){

    }
    public void setX(int num){
        x = num;
    }
    public void setY(int num){
        y = num;
    }
    public int getX(){
        return x;
    }
    public int getY(){
        return y;
    }
}
